#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    return self.dataSource.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"TEFansCell" forIndexPath:indexPath];
    
    // Configure the cell...
    TEUser* user = self.dataSource[indexPath.row];
    cell.textLabel.text = [NSString stringWithFormat:@"%lld-%@",user.userID,user.phoneNum];
    
    return cell;
}


